/**
 * Recursive type whose leaves are all `string`. Lets the EN literal stay
 * authoritative for keys while widening leaf types so other languages can
 * fill the same keys with their own translated strings.
 */
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string ? string : DeepStringify<T[K]>;
};

export type Translations = DeepStringify<typeof EN>;
